Map::Map() {
    space = TextureManager::LoadTexture("../data/img/space.png");
    dirt = TextureManager::LoadTexture("../data/img/dirt.png");
    debry = TextureManager::LoadTexture("../data/img/debry.png");

    LoadMap(lvl1);

    src.x = src.y = 0;
    src.w = dest.w = 32; // source and dest texture resolution is 32
    src.h = dest.h = 32;

    dest.x = dest.y = 0;

};
/////////////
Map::~Map() = default;

void Map::LoadMap(int arr[20][25]) {
    for (int row  = 0; row < 20; ++row) {
        for (int col = 0; col < 25; col++) {
            map[row][col] = arr[row][col];
        }
    }
}

void Map::DrawMap() {
    int type = 0;

    for (int row  = 0; row < 20; ++row) {
        for (int col = 0; col < 25; col++) {
            type = map[row][col];
            dest.x = col * 32; // need config file for these shits
            dest.y = row * 32;

            switch (type) {
                case 0:
                    TextureManager::Draw(space, src, dest);
                    break;
                case 1:
                    TextureManager::Draw(dirt, src, dest);
                    break;
                case 2:
                    TextureManager::Draw(debry, src, dest);
                    break;

                default: break;
            }

        }
    }
}

